                    prensa virtuales. El fútbol pasó a segundo plano y las agendas deportivas cubrían temas
                    coyunturales relacionados a la enfermedad como: la salud de los deportistas, el confinamiento
                    de atletas ecuatorianos en otros países, afectaciones en el calendario deportivo,
