                    Una mirada internacional
                </b>
                <br><br>
                <p style="line-height: 25pt;">

                    Al igual que Ecuador, el resto del mundo se vio afectado por la crisis del coronavirus.
                    El único diario deportivo de Argentina, Olé, también se adaptó a una nueva realidad. En él,
                    hubo dos cambios importantes. A nivel operativo, el trabajo pasó de ser físico a telemático. Normalmente
                    se reunián cara a cara en la redacción, pero ahora todo lo hacen desde casa con una computadora y
                    un grupo de WhatsApp. Otro cambio, fue el desafío de tener que hacer una página
                    de deportes cuando no existía información deportiva y la noticia principal era la COVID-19. Olé
                    contó noticias deportivas con un anclaje al virus. “Nos tomamos la licencia de informar sobre la
                    pandemia en un diario netamente deportivo. A las 19:00 horas todos los días informábamos el número de
                    contagiados por día en la nación Argentina”, dice Vicente Muglia, periodista del medio.

                    <br><br>
                    Lo que generó el coronavirus fue que se informara de acontecimientos que no tienen estrictamente
                    relación con el deporte. El diario ofreció un menú de alternativas para que las audiencias sigan
                    conectadas. En los meses de confinamiento se incrementó una sección destinada al coronavirus
                    y secciones como freestyle o eSports incrementaron el tráfico de visitas.
                </p>
            </div>
            <div class="col-md-6 ">
                <b class="titulos3">

                    Un vistazo hacia el futuro
                </b>
                <br><br>
                <p style="line-height: 25pt;">
                    El periodismo deportivo va encaminado hacia la innovación. El comunicador tiene la obligación de
                    estar a la vanguardia con los procesos tecnológicos y cambios en la comunicación, capacitarse y prepararse
                    para poder desarrollarse en diferentes áreas.
                    <br><br>
                    El deporte se ha caracterizado por ser un escenario de gran convocatoria, pero en un futuro eventos
                    masivos como ruedas de prensa, eventos deportivos o presentación de jugadores se podrían realizar
                    de manera virtual o con aforos muy limitados.
                    <br><br>
                    Los usuarios estaban acostumbrados a consumir información en una relación vertical de la comunicación,
                    donde los medios servían como depositantes de información. Con la pandemia eso ha cambiado, las audiencias
                    ahora tienen una relación más horizontal donde los consumidores pasan a ser prosumidores con una
                    retroalimentación constante.

                    En un punto de quiebre la pandemia aumentó el consumo de desinformación, por ello, el desafío para el
                    periodista deportivo es otorgarle al público noticias veraces y constrastadas.
                </p>




