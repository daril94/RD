                    <br>

                    <br>
                    Según la Federación Nacional de Periodistas del Ecuador (FENAPE), una de las áreas más
                    afectadas por la pandemia es la comunicación. Las secciones deportivas en los medios fueron
                    las primeras en cerrarse debido a la cancelación de eventos deportivos por la emergencia
                    sanitaria. Susana Piedra, presidenta de la FENAPE, afirma que muchos periodistas no
                    volvieron a ejercer su profesión. “Hay casos críticos de compañeros que dejaron de lado
                    la tarea periodística y se dedicaron a vender almuerzos para sobrevivir, es una situación
                    muy precaria”, expresó. <b><i>Registros de este oganismo revelan que aproximadamente 150
                            periodistas deportivos se encuentran sin trabajo a nivel nacional</b></i>.
                    <br><br>
                    La crisis generó recortes a nivel económico y de personal. “En nuestro país rige la famosa ley humanitaria,
                    que para muchos de humanitaria tiene muy poco, esta ley permite la flexibilización laboral,
                    reducciones salariales y cambios perjudiciales en los trabajadores”, señala Eduardo Andino,
                    periodista deportivo de GolTV y Desalineados.


